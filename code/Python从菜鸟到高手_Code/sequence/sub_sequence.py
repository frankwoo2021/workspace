numbers = [1,2,3,4,5,6,7,8]
print(numbers[3:5])				#  运行结果：[4, 5]
print(numbers[0:1])				#  运行结果：[1]
print(numbers[5:8])				#  运行结果：[6, 7, 8]
print(numbers[-3:-1])				#  运行结果：[6,7]
