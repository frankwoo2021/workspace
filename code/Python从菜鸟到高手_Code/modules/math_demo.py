import math

print('圆周率', '=', math.pi)
print('自然常数', '=', math.e)

print(math.fabs(-1.0))

print(math.ceil(1.3))  # 2  #这个方法对i向上取整，i = 1.3 ，返回值为2
print(math.floor(1.7)) #1 是向下取整。i = 1.4，返回1
print(math.pow(2,10)) # 返回a的b次方
print(math.sqrt(8))  # 8的平方根

print(math.sin(math.pi / 2))
print(math.cos(math.pi))
print(math.tan(math.pi / 4))


